package D_01;

public class SwitchM {
    public static void main(String[] args) {
        //switch 语句 byte/short/char/int
        int num = 3;
        switch (num) {
            case 1:
                System.out.println("AAA");
                break;
            case 2:
                System.out.println("BBB");
                break;
            default:
                System.out.println("CCC");
                break;
        }
    }
}
